def get_holidays(country_code: str, year: int) -> list[dict]:
    """Return holidays as list of {date, name} for a country+year."""
    country = HOLIDAYS.get(country_code.upper(), {})
    entries = country.get(str(year), [])
    return [{"date": d, "name": n} for d, n in entries]


def get_holiday_dates(country_code: str, year: int) -> set[str]:
    """Return set of date strings (YYYY-MM-DD) for quick lookup."""
    country = HOLIDAYS.get(country_code.upper(), {})
    return {d for d, _ in country.get(str(year), [])}
